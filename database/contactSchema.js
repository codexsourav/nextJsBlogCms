import "./db";
import mongoose from "mongoose";
const Contact = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
});
mongoose.models = {};
module.exports = mongoose.model("contact", Contact);
